package vo;

public class HouseholdVO {
	private String householdId;
	private String householdDate;
	private String householdHistory;
	private String householdInamt;
	private String householdOutamt;
	private String householdTotal;
	public String getHouseholdId() {
		return householdId;
	}
	public void setHouseholdId(String householdId) {
		this.householdId = householdId;
	}
	public String getHouseholdDate() {
		return householdDate;
	}
	public void setHouseholdDate(String householdDate) {
		this.householdDate = householdDate;
	}
	public String getHouseholdHistory() {
		return householdHistory;
	}
	public void setHouseholdHistory(String householdHistory) {
		this.householdHistory = householdHistory;
	}
	public String getHouseholdInamt() {
		return householdInamt;
	}
	public void setHouseholdInamt(String householdInamt) {
		this.householdInamt = householdInamt;
	}
	public String getHouseholdOutamt() {
		return householdOutamt;
	}
	public void setHouseholdOutamt(String householdOutamt) {
		this.householdOutamt = householdOutamt;
	}
	public String getHouseholdTotal() {
		return householdTotal;
	}
	public void setHouseholdTotal(String householdTotal) {
		this.householdTotal = householdTotal;
	}
	
}
